#include <iostream>
using namespace std;


// Arthimetic Operations


// Addition

int main() {
    int a,b;
    a=3;
    b=4;
    int c = a+b;
    cout<<c;
}

// Subtraction

int main() {
    int a,b;
    a=8;
    b=3;
    int c = a-b;
    cout<<c;
}


// Multiply

int main() {
    int a,b;
    a=3;
    b=4;
    int c = a*b;
    cout<<c;
}


// Division

int main() {
    int a,b;
    a=7;
    b=5;
    int c = a/b;
    cout<<c;
}


// Reminder

int main() {
    int a,b;
    a=18;
    b=4;
    int c = a%b;
    cout<<c;
}


// Post Increment

int main() {
    int a;
    a=10;
    cout<<a<<endl;
    a++;
    cout<<a;
}


// Post Decrement

int main() {
    int a;
    a=10;
    cout<<a<<endl;
    a--;
    cout<<a;
}


// Pre Increment

int main() {
    int a;
    a=10;
    cout<<a<<endl;
    ++a;
    cout<<a;
}


// Pre Decrement

int main() {
    int a;
    a=10;
    cout<<a<<endl;
    --a;
    cout<<a;
}


